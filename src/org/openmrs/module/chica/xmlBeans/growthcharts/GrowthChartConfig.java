package org.openmrs.module.chica.xmlBeans.growthcharts;


/**
 * Class to hold all the growth chart configurations
 *
 * @author Steve McKee
 */
public class GrowthChartConfig {
	
	private GrowthCharts growthCharts;

    /**
     * @return the growthCharts
     */
    public GrowthCharts getGrowthCharts() {
    	return growthCharts;
    }

    /**
     * @param growthCharts the growthCharts to set
     */
    public void setGrowthCharts(GrowthCharts growthCharts) {
    	this.growthCharts = growthCharts;
    }
}
